#include "libft.h"

char	*ft_strjoin(char *s1, char *s2)
{
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!str)
		return (NULL);
	else
	{
		ft_strcpy(str, s1);
		ft_strcat(str, s2);
	}
	free(s1);
	return (str);
}
